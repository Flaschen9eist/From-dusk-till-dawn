import turtle

#turtle.write('Turtle ausprobieren!', font=('Arial', 40, 'normal'))

turtle.width(8)

turtle.color('blue')
turtle.circle(100)

turtle.up()
turtle.color('orange')
turtle.forward(150)
turtle.down()
turtle.circle(100)

turtle.up()
turtle.color('green')
turtle.backward(300)
turtle.down()
turtle.circle(100)

turtle.done()
